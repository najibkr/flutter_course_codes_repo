mixin MathMixin {
  double addNumbers(double num1, double num2) {
    return num1 + num2;
  }

  double subtractNumbers(double num1, double num2) {
    return num1 - num2;
  }

  double multiplyNumbers(double num1, double num2) {
    return num1 * num2;
  }

  double divideNumbers(double num1, double num2) {
    return num1 / num2;
  }
}
